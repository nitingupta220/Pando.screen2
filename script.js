// 1st Input Box
document.getElementById("input").value = getSaveValue("input");

function saveValue(e) {
    var id = e.id;
    var val = e.value;
    localStorage.setItem(id, val);
}

function getSaveValue(v) {
    if (localStorage.getItem(v) === null) {
        return 8285698087;
    }
    return localStorage.getItem(v);
}

